<div class="section_faq" id="stips-internal-product-faq">
    <h2 class="product_text faq">Často kladené dotazy (FAQ):</h2>


    <div class="faq_item">
        <h3 class="faq_item_question">Jaký zvolit pro vyhlídkový let
            balonem oděv?</h3>
        <p class="faq_item_answer">
            Speciální oděv není pro vyhlídkový let balonem nutný.
            Doporučujeme pohodlné oblečení jako na procházku a
            pevnou obuv.
        </p>
    </div>

    <div class="faq_item">
        <h3 class="faq_item_question">Je v balonu zima?</h3>
        <p class="faq_item_answer">
            Při vyhlídkovém letu balonem na vás nebude foukat. Balon
            letí stejnou rychlostí jako vítr. Oblékněte se tedy
            podle počasí.
        </p>
    </div>

    <div class="faq_item">
        <h3 class="faq_item_question">Jak rychle balon letí?</h3>
        <p class="faq_item_answer">
            Vše záleží na rychlosti větru a aktuálních podmínkách.
            Za
            hodinový vyhlídkový let balonem se obvykle uletí pár
            desítek km. Při
            expedičním letu přes Alpy zvládne balon letět až 100
            km/hod. </p>
    </div>

    <div class="faq_item">
        <h3 class="faq_item_question">Mohou se vyhlídkového letu
            balonem účastnit i děti?</h3>
        <p class="faq_item_answer">
            Děti školního věku se mohou vyhlídkového letu balonem
            zúčastnit od výšky 130 cm. Je to tak především proto,
            aby viděly z koše
            ven a byly schopné poslouchat pilotovy instrukce. </p>
    </div>
</div>
